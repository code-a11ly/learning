// VARIABLE TYPES IN C:
#include <stdio.h>

int main()
{

    // RELATIONAL OPERATORS IN C:

    /*
    In all the languages we have the relational
    operators that basically compare things and
    gives us a answer so in C:

    && stands for AND relational operator

    || stands for OR relational operator

    > stands for greater than

    < stands for lessers than

    = stands for equal to

    There are too the:

    => equal OR greater than

    =< equal OR lesser than

    */

    int r, x = 5, y = 3;

    r = (x > 2) && (y > 0);

    printf("Result: %d\n", r);

    return 0;
}